package application;

public class Point {
	private double x, y;
	
	Point(double setX, double setY){
		x = setX;
		y = setY;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
}
